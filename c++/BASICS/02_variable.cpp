#include <iostream>
using namespace std;
int main() {
    // variables in c++
    // built-in data types
    int x = 5; // 4 BYTES, 4*8 = 32 BITS
    char y = 'A'; // 1 BYTE
    float z = 5.99f; // 4 BYTES
    bool isTrue = true; // 1 BYTE
    double pi = 3.14159; // 8 BYTES
    void *ptr = nullptr; // pointer type
    /* void: Represents the absence of a type; it cannot be used to declare a variable, 
    but is used for functions and pointers
    wchar_t: Wide character type, used for larger character sets */
}